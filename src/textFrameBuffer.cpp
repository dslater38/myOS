template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
void TextModeVideo<ROWS,COLS,DEPTH>::put(char c)
{
	switch(c)
	{
		case 0x08:
			if(cursor_x)
			{
				--cursor_x;
			}
			break;
		case 0x09:
			cursor_x = (cursor_x+8) & ~(8-1);
			break;
		case '\r':
			cursor_x = 0;
			break;
		case '\n':
			cursor_x = 0;
			++cursor_y;
			inc_cur_line();
			break;
		default:
			if(c >= ' ')
			{
				// The background colour is black (0), the foreground is white (15).
				// uint8_t backColour = 0;
				// uint8_t foreColour = 15;

				// The attribute byte is made up of two nibbles - the lower being the
				// foreground colour, and the upper the background colour.
				// uint8_t  attributeByte = color_attribute();
				// The attribute byte is the top 8 bits of the word we have to send to the
				// VGA board.
				// uint16_t attribute = attributeByte << 8;
				//  uint16_t *location = (uint16_t *)(video_memory + (cursor_y*COLS + cursor_x));
				uint16_t *location = (uint16_t *)(back_buffer + (cur_line*COLS + cursor_x*(DEPTH/sizeof(uint8_t))));
				*location = colored_char(c);
				// *location = (c | attribute);
				++cursor_x;				   
			}
			break;
	}

	// Check if we need to insert a new line because we have reached the end
	// of the screen.
	if (cursor_x >= COLS)
	{
		cursor_x = 0;
		cursor_y ++;
		inc_cur_line();
	}

	// Scroll the screen if needed.
	scroll();
	// copy the back buffer to frone
	copy_buffer2();
	// Move the hardware cursor.
	move_cursor();

}
template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
uint16_t TextModeVideo<ROWS,COLS,DEPTH>::color_attribute()const
{
	return (uint16_t)((backColor << 4) | (foreColor & 0x0F));
}

template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
uint16_t TextModeVideo<ROWS,COLS,DEPTH>::colored_char(char c)const
{
	return ((color_attribute() << 8) | c);
}

template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
void TextModeVideo<ROWS,COLS,DEPTH>::copy_buffer()const
{
	uint32_t *dst = (uint32_t *)video_memory;
	uint32_t *src=(uint32_t *)back_buffer;
	uint32_t *end = (uint32_t *)(back_buffer + ROWS*COLS);
	
	while(src<end)
	{
		*dst ++= *src++;
	}
}
template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
void TextModeVideo<ROWS,COLS,DEPTH>::copy_line2(int dstLine, int srcLine)const
{
	uint32_t *pDest = (uint32_t *)(video_memory + COLS*dstLine);
	uint32_t *pSrc= (uint32_t *)(back_buffer + COLS*srcLine);
	uint32_t *pEnd = pSrc + (COLS/2);
	while(pSrc<pEnd)
	{
		*pDest++ = *pSrc++;
	}
}

template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
void TextModeVideo<ROWS,COLS,DEPTH>::copy_buffer2()const
{
	int i;
	int j=0;
	if(cursor_y == (ROWS-1))
	{
		for(i=cur_line+1; i<ROWS; ++i )
		{
			copy_line2(j++,i);
		}
	}
	
	for(i=0; i<=cur_line; ++i )
	{
		copy_line2(j++,i);
	}
}


// Updates the hardware cursor.
template<const uint32_t ROWS, const uint32_t COLS, const uint32_t DEPTH>
void TextModeVideo<ROWS,COLS,DEPTH>::move_cursor()const
{
   // The screen is COLS characters wide...
   uint16_t cursorLocation = cursor_y * COLS + cursor_x;
   outb(0x3D4, 14);                  // Tell the VGA board we are setting the high cursor byte.
   outb(0x3D5, cursorLocation >> 8); // Send the high cursor byte.
   outb(0x3D4, 15);                  // Tell the VGA board we are setting the low cursor byte.
   outb(0x3D5, cursorLocation);      // Send the low cursor byte.
}



#define	VGA_AC_INDEX		0x3C0
#define	VGA_AC_WRITE		0x3C0
#define	VGA_AC_READ		0x3C1
#define	VGA_MISC_WRITE		0x3C2
#define VGA_SEQ_INDEX		0x3C4
#define VGA_SEQ_DATA		0x3C5
#define	VGA_DAC_READ_INDEX	0x3C7
#define	VGA_DAC_WRITE_INDEX	0x3C8
#define	VGA_DAC_DATA		0x3C9
#define	VGA_MISC_READ		0x3CC
#define VGA_GC_INDEX 		0x3CE
#define VGA_GC_DATA 		0x3CF
/*			COLOR emulation		MONO emulation */
#define VGA_CRTC_INDEX		0x3D4		/* 0x3B4 */
#define VGA_CRTC_DATA		0x3D5		/* 0x3B5 */
#define	VGA_INSTAT_READ		0x3DA

#define	VGA_NUM_SEQ_REGS	5
#define	VGA_NUM_CRTC_REGS	25
#define	VGA_NUM_GC_REGS		9
#define	VGA_NUM_AC_REGS		21
#define	VGA_NUM_REGS		(1 + VGA_NUM_SEQ_REGS + VGA_NUM_CRTC_REGS + \
				VGA_NUM_GC_REGS + VGA_NUM_AC_REGS)

#define	peekb(S,O)		*(unsigned char *)(16uL * (S) + (O))
#define	pokeb(S,O,V)		*(unsigned char *)(16uL * (S) + (O)) = (V)
#define	pokew(S,O,V)		*(unsigned short *)(16uL * (S) + (O)) = (V)
#define	_vmemwr(DS,DO,S,N)	memcpy((char *)((DS) * 16 + (DO)), S, N)


/*****************************************************************************/
void write_regs(unsigned char *regs)
{
	unsigned i;

/* write MISCELLANEOUS reg */
	outb(VGA_MISC_WRITE, *regs);
	regs++;
/* write SEQUENCER regs */
	for(i = 0; i < VGA_NUM_SEQ_REGS; i++)
	{
		outb(VGA_SEQ_INDEX, i);
		outb(VGA_SEQ_DATA, *regs);
		regs++;
	}
/* unlock CRTC registers */
	outb(VGA_CRTC_INDEX, 0x03);
	outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) | 0x80);
	outb(VGA_CRTC_INDEX, 0x11);
	outb(VGA_CRTC_DATA, inb(VGA_CRTC_DATA) & ~0x80);
/* make sure they remain unlocked */
	regs[0x03] |= 0x80;
	regs[0x11] &= ~0x80;
/* write CRTC regs */
	for(i = 0; i < VGA_NUM_CRTC_REGS; i++)
	{
		outb(VGA_CRTC_INDEX, i);
		outb(VGA_CRTC_DATA, *regs);
		regs++;
	}
/* write GRAPHICS CONTROLLER regs */
	for(i = 0; i < VGA_NUM_GC_REGS; i++)
	{
		outb(VGA_GC_INDEX, i);
		outb(VGA_GC_DATA, *regs);
		regs++;
	}
/* write ATTRIBUTE CONTROLLER regs */
	for(i = 0; i < VGA_NUM_AC_REGS; i++)
	{
		(void)inb(VGA_INSTAT_READ);
		outb(VGA_AC_INDEX, i);
		outb(VGA_AC_WRITE, *regs);
		regs++;
	}
/* lock 16-color palette and unblank display */
	(void)inb(VGA_INSTAT_READ);
	outb(VGA_AC_INDEX, 0x20);
}

/*****************************************************************************
*****************************************************************************/
static void set_plane(unsigned p)
{
	unsigned char pmask;

	p &= 3;
	pmask = 1 << p;
/* set read plane */
	outb(VGA_GC_INDEX, 4);
	outb(VGA_GC_DATA, p);
/* set write plane */
	outb(VGA_SEQ_INDEX, 2);
	outb(VGA_SEQ_DATA, pmask);
}

/*****************************************************************************
VGA framebuffer is at A000:0000, B000:0000, or B800:0000
depending on bits in GC 6
*****************************************************************************/
static unsigned get_fb_seg(void)
{
	unsigned seg;

	outb(VGA_GC_INDEX, 6);
	seg = inb(VGA_GC_DATA);
	seg >>= 2;
	seg &= 3;
	switch(seg)
	{
	case 0:
	case 1:
		seg = 0xA000;
		break;
	case 2:
		seg = 0xB000;
		break;
	case 3:
		seg = 0xB800;
		break;
	}
	return seg;
}

/*****************************************************************************
*****************************************************************************/
static void vmemwr(unsigned dst_off, unsigned char *src, unsigned count)
{
	_vmemwr(get_fb_seg(), dst_off, src, count);
}

/*****************************************************************************
write font to plane P4 (assuming planes are named P1, P2, P4, P8)
*****************************************************************************/
static void write_font(unsigned char *buf, unsigned font_height)
{
	unsigned char seq2, seq4, gc4, gc5, gc6;
	unsigned i;

/* save registers
set_plane() modifies GC 4 and SEQ 2, so save them as well */
	outb(VGA_SEQ_INDEX, 2);
	seq2 = inb(VGA_SEQ_DATA);

	outb(VGA_SEQ_INDEX, 4);
	seq4 = inb(VGA_SEQ_DATA);
/* turn off even-odd addressing (set flat addressing)
assume: chain-4 addressing already off */
	outb(VGA_SEQ_DATA, seq4 | 0x04);

	outb(VGA_GC_INDEX, 4);
	gc4 = inb(VGA_GC_DATA);

	outb(VGA_GC_INDEX, 5);
	gc5 = inb(VGA_GC_DATA);
/* turn off even-odd addressing */
	outb(VGA_GC_DATA, gc5 & ~0x10);

	outb(VGA_GC_INDEX, 6);
	gc6 = inb(VGA_GC_DATA);
/* turn off even-odd addressing */
	outb(VGA_GC_DATA, gc6 & ~0x02);
/* write font to plane P4 */
	set_plane(2);
/* write font 0 */
	for(i = 0; i < 256; i++)
	{
		vmemwr(16384u * 0 + i * 32, buf, font_height);
		buf += font_height;
	}
#if 0
/* write font 1 */
	for(i = 0; i < 256; i++)
	{
		vmemwr(16384u * 1 + i * 32, buf, font_height);
		buf += font_height;
	}
#endif
/* restore registers */
	outb(VGA_SEQ_INDEX, 2);
	outb(VGA_SEQ_DATA, seq2);
	outb(VGA_SEQ_INDEX, 4);
	outb(VGA_SEQ_DATA, seq4);
	outb(VGA_GC_INDEX, 4);
	outb(VGA_GC_DATA, gc4);
	outb(VGA_GC_INDEX, 5);
	outb(VGA_GC_DATA, gc5);
	outb(VGA_GC_INDEX, 6);
	outb(VGA_GC_DATA, gc6);
}

